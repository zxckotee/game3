#!/bin/bash
# Скрипт для запуска исправления системы торговцев

# Переходим в корневой каталог проекта
cd "$(dirname "$0")/.."

# Проверяем наличие Node.js
if ! command -v node &> /dev/null; then
    echo "Ошибка: Node.js не найден. Пожалуйста, установите Node.js."
    exit 1
fi

# Запускаем скрипт JavaScript
echo "Запуск исправления системы торговцев..."
node scripts/fix_merchant_system.js

# Проверяем код возврата
if [ $? -eq 0 ]; then
    echo "Исправление системы торговцев успешно завершено!"
else
    echo "Произошла ошибка при исправлении системы торговцев."
    exit 1
fi

echo "Все изменения успешно применены к базе данных."